CREATE TABLE LIBRARY(
    
    LIBRARY_ID VARCHAR2(10) PRIMARY KEY, --Easy way to define primary key
    NAME VARCHAR2 (30) NOT NULL, -- This can not be null, Have to input
    LOCATION VARCHAR2 (50),
    CAPACITY NUMBER DEFAULT 5000, -- If nothing inserted iw will be dafault 5000
    -- CONSTRAINT LIB_ID_PK PRIMARY KEY (LIBRARY_ID) -- Complex way to define primary key
    CONSTRAINT CAPACITY_CHECK CHECK(CAPACITY > 0) --Check whether the capacityb is more than 0 or not, if not the info will not created

);

