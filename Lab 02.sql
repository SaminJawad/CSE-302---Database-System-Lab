CREATE TABLE BOOK(
    
    BOOK_ID VARCHAR2(10) PRIMARY KEY, --Easy way to define primary key
    TITLE VARCHAR2 (30) NOT NULL, -- This can not be null, Have to input
    LIBRARY_ID VARCHAR2 (50),
    COPIES NUMBER,
    CONSTRAINT COPIES_CHECK CHECK(COPIES >= 1), --Check whether the capacityb is more than 0 or not, if not the info will not created
    FOREIGN KEY (LIBRARY_ID) REFERENCES LIBRARY (LIBRARY_ID) --Foreign Key Defined
    
);

